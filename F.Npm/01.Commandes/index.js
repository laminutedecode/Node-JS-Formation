// npm init : Initialise un nouveau projet Node.js en créant un fichier package.json.

// npm install [package] : Installe un package depuis le registre npm. Par exemple : npm install express.

// npm install [package] --save : Installe un package et l'ajoute en tant que dépendance dans le fichier package.json.

// npm install [package] --save-dev : Installe un package et l'ajoute en tant que dépendance de développement dans le fichier package.json.

// npm uninstall [package] : Désinstalle un package. Par exemple : npm uninstall express.

// npm update [package] : Met à jour un package. Par exemple : npm update express.

// npm list : Affiche une liste des packages installés localement.

// npm list -g : Affiche une liste des packages installés globalement.

// npm outdated : Affiche une liste des packages obsolètes.

// npm search [keyword] : Recherche des packages sur le registre npm. Par exemple : npm search lodash.

// npm start : Lance l'application en utilisant le script de démarrage défini dans le fichier package.json.

// npm stop : Arrête l'application.

// npm help : Affiche l'aide pour les commandes npm.

// npm cache clean : Nettoie le cache npm.

// npm publish : Publie un package sur le registre npm.


// npm run [script-name] : Exécute un script défini dans le fichier package.json.

// npm test : Exécute les tests définis dans le fichier package.json.

// npm version [patch | minor | major] : Met à jour la version du package dans le fichier package.json et crée un tag git. Par exemple : npm version patch.

// npm login : Connecte l'utilisateur au registre npm.

// npm logout : Déconnecte l'utilisateur du registre npm.